var option_count = 0

for (let i = 0; i < item_load.length; i++) {
    $('.item_picture')[i].setAttribute('src', `./img/item/item${i + 1}-1.jpg`);
    $('.item_brand')[i].textContent = item_load[i].brand
    $('.item_name')[i].textContent = item_load[i].name
    $('.item_price')[i].textContent = item_load[i].price

    //load option
    if (item_load[i].option > 1) {
        for (let j = 0; j < Number(item_load[i].option); j++) {
            $('.option-picture')[option_count].setAttribute('src', `./img/item/item${i + 1}-${j + 1}.jpg`)
            option_count++;
        }
    } else {
        $('.item_options')[i].style.display = 'none'
    }

    //load size
    const l_item_size = item_load[i].size
    if (l_item_size.length > 0) {
        for (let k = 0; k < l_item_size.length; k++) {
            const span = document.createElement('span')
            span.innerHTML = `${l_item_size[k]}`
            span.setAttribute('class', `size ${k == 0 ? 'active-size' : ''}`)
            $('.item-size')[i].appendChild(span)
        }
    } else {
        $('.item-size')[i].style.display = 'none'
    }

    //load rating
    //load full star
    for (let g = 0; g < item_load[i].rating; g++) {
        const tag = document.createElement('i')
        tag.setAttribute('class', `bi bi-star-fill`)
        $('.item_rating')[i].appendChild(tag)
    }
    //load star
    for (let g = 0; g < 5 - item_load[i].rating; g++) {
        const tag = document.createElement('i')
        tag.setAttribute('class', `bi bi-star`)
        $('.item_rating')[i].appendChild(tag)
    }
}

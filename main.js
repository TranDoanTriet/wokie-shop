function checkMarked(check) {
    const checked = check.toLowerCase()
    if (checked == 'new') {
        return 'tt-n'
    }
    if (checked == 'popular' || check == 'featured') {
        return 'tt-p'
    }
    if (checked == 'sale') {
        return 'tt-s'
    }
}

